class LocationChecker {
  final List<Map<String, dynamic>> cityTaguig;
  final List<Map<String, dynamic>> taguigBarangays;

  LocationChecker(this.cityTaguig, this.taguigBarangays);
  Map<String, String> searchUser(double latitude, double longitude) {
    if (latitude == '' || longitude == '') {
      return {'error': 'Latitude and longitude are required.'};
    }

    // Check if the location is inside Taguig City
    if (isPointInArray(latitude, longitude, cityTaguig)) {
      String insideBarangay = '';
      double minDistance = double.infinity;

      for (var barangay in taguigBarangays) {
        List<List<double>> boundary = parseBoundary(barangay['boundary']);

        if (isPointInPolygon(latitude, longitude, boundary)) {
          return {'exact': barangay['name']};
        }

        double distance =
            calculateDistanceToBoundary(latitude, longitude, boundary);

        if (distance < minDistance) {
          minDistance = distance;
          insideBarangay = barangay['name'];
        }
      }

      if (insideBarangay != '') {
        return {'near': insideBarangay};
      } else {
        return {'error': 'No barangays with boundaries found.'};
      }
    } else {
      return {
        'Unauthorized':
            'This application is only for the citizens of Taguig City'
      };
    }
  }

  bool isPointInArray(double latitude, double longitude,
      List<Map<String, dynamic>> coordinates) {
    bool isInside = false;
    for (var coordinate in coordinates) {
      if (coordinate['boundary'] != null) {
        List<List<double>> boundary = parseBoundary(coordinate['boundary']);
        if (isPointInPolygon(latitude, longitude, boundary)) {
          isInside = true;
          break;
        }
      }
    }
    return isInside;
  }

  List<List<double>> parseBoundary(dynamic boundary) {
    List<List<double>> result = [];
    for (var point in boundary) {
      if (point is List<dynamic> && point.length == 2) {
        double lat = point[0];
        double lng = point[1];
        result.add([lat, lng]);
      }
    }
    return result;
  }

  bool isPointInPolygon(
      double latitude, double longitude, List<List<double>> points) {
    int count = points.length;
    bool isInside = false;

    for (int i = 0, j = count - 1; i < count; j = i++) {
      double lat1 = points[i][0];
      double lng1 = points[i][1];
      double lat2 = points[j][0];
      double lng2 = points[j][1];

      if (((lat1 > latitude) != (lat2 > latitude)) &&
          (longitude <
              (lng2 - lng1) * (latitude - lat1) / (lat2 - lat1) + lng1)) {
        isInside = !isInside;
      }
    }

    return isInside;
  }

  double calculateDistanceToBoundary(
      double latitude, double longitude, List<List<double>> points) {
    double minDistance = double.infinity;

    for (int i = 0; i < points.length; i++) {
      double lat1 = points[i][0];
      double lng1 = points[i][1];
      double lat2 = points[(i + 1) % points.length][0];
      double lng2 = points[(i + 1) % points.length][1];

      double distance = calculatePointToLineDistance(
          latitude, longitude, lat1, lng1, lat2, lng2);

      if (distance < minDistance) {
        minDistance = distance;
      }
    }

    return minDistance;
  }

  double calculatePointToLineDistance(double lat, double lng, double lat1,
      double lng1, double lat2, double lng2) {
    double a = lat - lat1;
    double b = lng - lng1;
    double c = lat2 - lat1;
    double d = lng2 - lng1;

    double dot = a * c + b * d;
    double lenSq = c * c + d * d;

    if (lenSq == 0) {
      return calculateHaversineDistance(lat, lng, lat1, lng1);
    }

    double param = dot / lenSq;

    if (param < 0) {
      return calculateHaversineDistance(lat, lng, lat1, lng1);
    } else if (param > 1) {
      return calculateHaversineDistance(lat, lng, lat2, lng2);
    }

    double xx = lat1 + param * c;
    double yy = lng1 + param * d;

    return calculateHaversineDistance(lat, lng, xx, yy);
  }

  double calculateHaversineDistance(
      double lat1, double lng1, double lat2, double lng2) {
    double earthRadius = 6371; // Radius of the Earth in kilometers
    double lat1Rad = degreesToRadians(lat1);
    double lng1Rad = degreesToRadians(lng1);
    double lat2Rad = degreesToRadians(lat2);
    double lng2Rad = degreesToRadians(lng2);

    double dLat = lat2Rad - lat1Rad;
    double dLng = lng2Rad - lng1Rad;

    double a = sin(dLat / 2) * sin(dLat / 2) +
        cos(lat1Rad) * cos(lat2Rad) * sin(dLng / 2) * sin(dLng / 2);
    double c = 2 * atan2(sqrt(a), sqrt(1 - a));
    double distance = earthRadius * c;

    return distance;
  }

  double degreesToRadians(double degrees) {
    return degrees * (pi / 180);
  }
}

class LocationService {
  LocationChecker locationChecker;

  LocationService(this.locationChecker);

  Future<Map<String, String?>> getUserLocation(
      double latitude, double longitude) async {
    Map<String, String> result =
        locationChecker.searchUser(latitude, longitude);

    if (result.containsKey('exact')) {
      return {'type': 'exact', 'value': result['exact']};
    } else if (result.containsKey('near')) {
      return {'type': 'near', 'value': result['near']};
    } else {
      return {'type': 'Unauthorized', 'value': null};
    }
  }
}
